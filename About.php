			<p>Albania (/ælˈbeɪniə, ɔːl-/ (About this soundlisten) a(w)l-BAY-nee-ə; Albanian: Shqipëri or Shqipëria),[a] officially the Republic of Albania (Albanian: Republika e Shqipërisë),[b] is a country in Southeastern Europe. It is located on the Adriatic and Ionian Sea within the Mediterranean Sea, and shares land borders with Montenegro to the northwest, Kosovo[c] to the northeast, North Macedonia to the east, Greece to the south; and maritime borders with Greece, Montenegro and Italy to the west. Tirana is its capital and largest city, followed by Durrës, Vlorë and Shkodër.
			Geographically, Albania displays varied climatic, geological, hydrological, and morphological conditions, defined in an area of 28,748 km2 (11,100 sq mi). It possesses significant diversity with the landscape ranging from the snow-capped mountains in the Albanian Alps as well as the Korab, Skanderbeg, Pindus and Ceraunian Mountains to the hot and sunny coasts of the Albanian Adriatic and Ionian Sea along the Mediterranean Sea.

            Historically, Albania has been inhabited by numerous civilisations such as the Illyrians, Thracians, Ancient Greeks, Romans, Byzantines, Venetians and Ottomans. The Albanians established the autonomous Principality of Arbër in the 12th century. The Kingdom of Albania and Principality of Albania formed between the 13th and 14th centuries. Prior to the Ottoman conquest of Albania in the 15th century, the Albanian resistance to Ottoman expansion into Europe led by Gjergj Kastrioti Skanderbeg won them acclaim over most of Europe. Between the 18th and 19th centuries, cultural developments, widely attributed to Albanians having gathered both spiritual and intellectual strength, conclusively led to the Albanian Renaissance. After the defeat of the Ottomans in the Balkan Wars, the modern nation state of Albania declared independence in 1912.[7] In the 20th century, the Kingdom of Albania was invaded by Italy which formed Greater Albania before becoming a protectorate of Nazi Germany.[8] Enver Hoxha formed Communist Albania after World War II and launched the Albanians on a path of oppression and decades of isolation. The Revolutions of 1991 concluded the fall of communism in Albania and eventually the establishment of the current Republic of Albania.

            Politically, Albania is a unitary parliamentary constitutional republic and a developing country with an upper-middle income economy dominated by the service sector, followed by manufacturing.[9] It went through a process of transition following the end of communism in 1990, from centralized planning to a market-based economy.[10][11][12] Albania provides universal health care and free primary and secondary education to its citizens.[5] Albania is a member of the United Nations, World Bank, UNESCO, NATO, WTO, COE, OSCE, and OIC. It is an official candidate for membership in the European Union.[13] It is one of the founding members of the Energy Community, including the Organization of the Black Sea Economic Cooperation and Union for the Mediterranean.  </p>

