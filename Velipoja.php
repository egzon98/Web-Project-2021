			<p id="tregimi">
				Velipoja është një fshat në komunën Velipojë në rrethin e Shkodrës të Shqipërisë.
				Derdhja e Bunës në veri të vendit shënjon drejt jugut vijën bregdetare shqiptare. Ranishtja më veriore e vendit është ajo e Velipojës, që gjendet rreth 25 km larg qytetit të Shkodër. Këtu ka mbi 250 ditë me diell. Gjatë verës temperaturat arrijnë mbi 30 °C. Pranë ranishtes të Velipojës ndodhet edhe laguna e Vilunit me një sipërfaqe prej rreth 130 hektarë. Ky është një ekosistem i rëndësishëm për folëzimin e shpendëve ujorë, me 183 lloje ; ndodhet edhe një rezervat gjuetie prej 700 hektarë.
				Velipoja është një qendër turistike mjaft e këndshme, me peizazh të larmishëm, ku ndërthuren hijeshim det, ranishtja, lumi, delta me ishuj, kodra, pylli, laguna, fusha, ligatinat ; vendi më i përshtatshëm për të vrojtuar zogjtë e Adriatikut Lindor në dunat e rërës dhe pellgjet e cekta të ujit ; trevë me popullim të hershëm, tradita të pasura etno-folklorike, me fermerë dhe peshkatarë të zotë, mikpritës të njohur.

				Velipoja u ofron pushuesve mundësinë për të kaluar pushime në një vijë bregdetare me ujë të pastër dhe me rërë të imët, por jo vetëm kaq, ajo është e pasur me vlera unike natyrore, në të cilat dallohen : Ranishtja e madhe me gjatësi 14 km dhe gjerësi 50–200 m me rërë të imët dhe të pastër, e cila quhet nga banorët “râna e dermanit” për vetitë e saja kuruese. Plazhi ripërtërihet çdo vit nga baticat dhe zbaticat. Rëra e bregut të të gjithë ranishtes ka përqindjen më të lartë të jodit në të gjithë bregdetin shqiptar. Plazhi rrethohet nga pylli me drurë të lartë pishe e drurë të tjerë halorë e fletor, me shkurre përherë të blerta, lugina plot me bimësi ujore, duna, etj.

				Fshati bregdetar, ranishtja e madhe dhe qendra turistike shtrihen në jugperëndim të Shkodrës, relievi është gati tërësisht fushor, nga 1-3 metra mbi nivelin e detit deri ne 550 metra.

				Velipoja ka një klimë të butë (temperatura mesatare 6-7 °C), vera është mjaft e nxehtë (temperatura mesatare e korrikut, 24 °C deri ne 48 °C píku në periudhën korrik–gusht). Periudha e përshtatshme për banjo dielli fillon ne muajin qershor dhe vazhdon deri në fund të shtatorit. Bien rreth 1400 mm reshje në vit, kryesisht të përqendruara në gjysmën e dytë të vjeshtës, dhe dimrit.

				Emri Velipojë vjen nga sllavishtja e vjetër (Velikopoje – Fushë e Madhe). Zona turistike shtrihet në një sipërfaqe 300 ha dhe kufizohet në veri me rezervatin e Menaxhuar të Velipojës, në jug me detin Adriatik, në lindje me rrethin e Lezhës dhe në perëndim me lumin Buna.  
